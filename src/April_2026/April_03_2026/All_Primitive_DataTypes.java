/*
*       This code block contains list of all the primitive data types
*       and their definitions
 */


public class All_Primitive_DataTypes {

    public static void main(String[] args) {

        byte a = -128;     // byte can only hold from -128 to 127
        byte b = 127;
        byte c = 'z';      // The variable c will return the ASCII value of 'z'

        char n = 'a';   // char holds single characters
        char o = 123;   // this will print the character that has ASCII value 123

        short d = 32767;   // short can hold values from -32768 to 32767
        short e = -32768;

        int f = 2147483647;
        int g = -2147483648;   // int stores from -2 billion to +2 billion

        long h = 9223372036854775807L;
        long i = -9223372036854775808L;

        boolean j = true;
        boolean k = false;

        float l = 34324524524622334535647967455147483647.2F;
        double m = 4.5;



        System.out.println(l);
        System.out.println(n);


        //System.out.println(c);


    }

}
